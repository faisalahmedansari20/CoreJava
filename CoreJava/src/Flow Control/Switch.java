class Switch
{
	public static void main(String[] args)
	{
		int x=97;
//		final int y=10;
//        byte x=20;
		switch(x)
		{
			case 98:
				System.out.println("it is 21");
			      break;

            case 'a' :
				   System.out.println("it is a 97");
			       break;

            case 10:
				   System.out.println("it is 20");
			       break;

		
		default :
			 System.out.println("in the default ");
		     break ;
		}
	}

}